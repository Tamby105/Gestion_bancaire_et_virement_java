    private void search() {
    String searchText = jTextField5.getText().trim();

    try {
        int red = 45; // Red component (0-255)
        int green = 95; // Green component (0-255)
        int blue = 95; // Blue component (0-255)

        JTableHeader thead = tblclient.getTableHeader();
        Color rgbColor = new Color(red, green, blue);
        Color rgbColor2 = new Color(69, 169, 157);
        thead.setBackground(rgbColor);
        thead.setFont(new Font("Nirmala UI", Font.BOLD, 22));
        thead.setForeground(rgbColor2);

        DefaultTableModel dt = (DefaultTableModel) tblclient.getModel();
        dt.setRowCount(0);

        String sql = "SELECT * FROM client WHERE (numCompte LIKE ? OR Nom LIKE ? OR Prenoms LIKE ?) AND Statut = ?";

            String statut = "oui"; // Valeur par défaut
            if (radio_actif.isSelected()) {
                statut = "oui";
            } else if (radio_inactif.isSelected()) {
                statut = "non";
            }
        try (Connection conn = models.MConnexion.SiConnectionDB();
             PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setString(1, "%" + searchText + "%");
            stmt.setString(2, "%" + searchText + "%");
            stmt.setString(3, "%" + searchText + "%");
            stmt.setString(4, statut);
            try (ResultSet rs = stmt.executeQuery()) {
                while (rs.next()) {
                    Vector<String> v = new Vector<>();
                    v.add(rs.getString(1));
                    v.add(rs.getString(2));
                    v.add(rs.getString(3));
                    v.add(rs.getString(4));
                    v.add(rs.getString(5));
                    v.add(rs.getString(6));
                    v.add(rs.getString(7));
                    

                    dt.addRow(v);
                }
            }
        }

    } catch (Exception e) {
        e.printStackTrace(); // Affiche l'exception dans la console
    }
}
    
